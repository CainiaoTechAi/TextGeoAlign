def get_tensorboard_writer(args, tensorboard_path):
    """
    开启tensorboard，非分布式，或分布式情况下的master卡
    """
    if args.close_tensorboard:
        return None
    if args.multiprocessing_distributed and args.global_rank > 0:
        return None
    writer = SummaryWriter(log_dir=tensorboard_path, comment="")
    return writer


